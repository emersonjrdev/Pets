class Pet {
  final String name;
  final String image;
  final String location;
  final String sex;
  final String color;
  final String breed;
  final double weight;
  final String description;


  Pet({
    required this.name,
    required this.image,
    required this.location,
    required this.sex,
    required this.color,
    required this.breed,
    required this.weight,
    required this.description,
  });
}
